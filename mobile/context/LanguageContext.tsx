export type Language = 'en' | 'pt' | 'es' | 'fr' | 'de' | 'it';

// Função para detectar o idioma do dispositivo
const getDeviceLanguage = (): Language => {
  let locale: string = 'en';
  
  try {
    // Tentar usar Intl API (mais confiável e funciona em todas as plataformas)
    if (typeof Intl !== 'undefined' && Intl.DateTimeFormat) {
      const resolved = Intl.DateTimeFormat().resolvedOptions();
      locale = resolved.locale || resolved.language || 'en';
    }
    
    // Fallback para NativeModules se Intl não funcionar ou retornar 'en'
    // Mas também tentar sempre NativeModules para ter certeza
    if (Platform.OS === 'ios') {
      const SettingsManager = NativeModules.SettingsManager;
      if (SettingsManager?.settings) {
        const iosLocale = SettingsManager.settings.AppleLocale || 
                 SettingsManager.settings.AppleLanguages?.[0];
        if (iosLocale && iosLocale !== 'en') {
          locale = iosLocale;
        }
      }
    } else if (Platform.OS === 'android') {
      const I18nManager = NativeModules.I18nManager;
      if (I18nManager?.localeIdentifier) {
        const androidLocale = I18nManager.localeIdentifier;
        if (androidLocale && androidLocale !== 'en') {
          locale = androidLocale;
        }
      }
    }
  } catch (error) {
    locale = 'en';
  }
  
  const languageCode = locale.split('-')[0].split('_')[0].toLowerCase();
  const supportedLanguages: Language[] = ['en', 'pt', 'es', 'fr', 'de', 'it'];
  if (supportedLanguages.includes(languageCode as Language)) {
    return languageCode as Language;
  }
  return 'en';
};

